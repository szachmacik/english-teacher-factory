function generateProductMarkdown(product: Record<string, unknown>): string {
  let content: Record<string, unknown> = {};
  try {
    content = typeof product.content === "string" ? JSON.parse(product.content) : (product.content as Record<string, unknown>) || {};
  } catch {
    content = {};
  }

  const lines: string[] = [
    `# ${product.title || product.type}`,
    ``,
    `**Type:** ${product.type}`,
    `**Status:** ${product.status}`,
    ``,
    `---`,
    ``,
  ];

  // Render content based on type
  if (content.instructions) lines.push(`## Instructions\n\n${content.instructions}\n`);
  if (content.objectives) lines.push(`## Learning Objectives\n\n${Array.isArray(content.objectives) ? content.objectives.map((o: unknown) => `- ${o}`).join("\n") : content.objectives}\n`);
  if (content.vocabulary) lines.push(`## Vocabulary\n\n${Array.isArray(content.vocabulary) ? content.vocabulary.map((v: unknown) => typeof v === "object" ? `- **${(v as Record<string, unknown>).word}** — ${(v as Record<string, unknown>).definition}` : `- ${v}`).join("\n") : content.vocabulary}\n`);
  if (content.exercises) lines.push(`## Exercises\n\n${Array.isArray(content.exercises) ? content.exercises.map((e: unknown, i: number) => `### Exercise ${i + 1}\n${typeof e === "object" ? JSON.stringify(e, null, 2) : e}`).join("\n\n") : content.exercises}\n`);
  if (content.questions) lines.push(`## Questions\n\n${Array.isArray(content.questions) ? content.questions.map((q: unknown, i: number) => `${i + 1}. ${typeof q === "object" ? (q as Record<string, unknown>).question || JSON.stringify(q) : q}`).join("\n") : content.questions}\n`);
  if (content.answers) lines.push(`## Answer Key\n\n${Array.isArray(content.answers) ? content.answers.map((a: unknown, i: number) => `${i + 1}. ${a}`).join("\n") : content.answers}\n`);
  if (content.teacherNotes) lines.push(`## Teacher Notes\n\n${content.teacherNotes}\n`);
  if (content.timeRequired) lines.push(`**Estimated Time:** ${content.timeRequired}\n`);

  // Fallback: dump all content
  const knownKeys = ["instructions", "objectives", "vocabulary", "exercises", "questions", "answers", "teacherNotes", "timeRequired"];
  for (const [key, value] of Object.entries(content)) {
    if (!knownKeys.includes(key) && value) {
      lines.push(`## ${key.charAt(0).toUpperCase() + key.slice(1)}\n\n${typeof value === "object" ? JSON.stringify(value, null, 2) : value}\n`);
    }
  }

  return lines.join("\n");
}

function generateProductPlainText(product: Record<string, unknown>): string {
  let content: Record<string, unknown> = {};
  try {
    content = typeof product.content === "string" ? JSON.parse(product.content) : (product.content as Record<string, unknown>) || {};
  } catch {
    content = {};
  }
  return `${product.title || product.type}\n${"=".repeat(60)}\n\n${JSON.stringify(content, null, 2)}`;
}

function generateProductsIndex(productList: Record<string, unknown>[]): string {
  const lines = ["# Products Index\n", `Total: ${productList.length} products\n`, "| # | Type | Title | Status |", "|---|------|-------|--------|"];
  productList.forEach((p, i) => {
    lines.push(`| ${i + 1} | ${p.type} | ${p.title || "—"} | ${p.status} |`);
  });
  return lines.join("\n");
}

function generateVocabCSV(vocab: Record<string, unknown>[]): string {
  const header = "Word,Part of Speech,Definition,Example Sentence,Polish Translation,CEFR Level";
  const rows = vocab.map((v) =>
    [v.word, v.partOfSpeech, v.definition, v.exampleSentence, v.polishTranslation, v.cefrLevel]
      .map((cell) => `"${String(cell || "").replace(/"/g, '""')}"`)
      .join(",")
  );
  return [header, ...rows].join("\n");
}

function generateAnkiDeck(vocab: Record<string, unknown>[]): string {
  const header = "#separator:tab\n#html:false\n#notetype:Basic\n";
  const rows = vocab.map((v) => `${v.word}\t${v.definition} | Example: ${v.exampleSentence || ""}`);
  return header + rows.join("\n");
}

function generateVocabMarkdown(vocab: Record<string, unknown>[]): string {
  const lines = ["# Vocabulary Reference\n", `${vocab.length} words\n`];
  for (const v of vocab) {
    lines.push(`## ${v.word} *(${v.partOfSpeech || "n/a"})*`);
    lines.push(`**Definition:** ${v.definition}`);
    if (v.exampleSentence) lines.push(`**Example:** *${v.exampleSentence}*`);
    if (v.polishTranslation) lines.push(`**Polish:** ${v.polishTranslation}`);
    if (v.cefrLevel) lines.push(`**CEFR:** ${v.cefrLevel}`);
    lines.push("");
  }
  return lines.join("\n");
}

function generateGamesIndex(gameList: Record<string, unknown>[]): string {
  const lines = [
    "# Interactive Games",
    "",
    "Share these links with your students — no login required!",
    "",
    "| Game Type | Share Link | Plays |",
    "|-----------|-----------|-------|",
  ];
  for (const g of gameList) {
    const link = g.shareToken ? `/game/${g.shareToken}` : "—";
    lines.push(`| ${g.type} | ${link} | ${g.plays || 0} |`);
  }
  lines.push("\n> **Note:** Links are relative to your EduFactory domain.");
  return lines.join("\n");
}

