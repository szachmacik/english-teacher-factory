import archiver from "archiver";
import { PassThrough } from "stream";
import { getDb } from "../db";
import { products, projects, games, vocabularyItems } from "../../drizzle/schema";
import { eq, and } from "drizzle-orm";

export interface ZipBundleOptions {
  projectId: number;
  userId: number;
  includeProducts?: boolean;
  includeGames?: boolean;
  includeVocabulary?: boolean;
}

export async function generateProjectZipBundle(options: ZipBundleOptions): Promise<{ stream: PassThrough; filename: string }> {
  const { projectId, userId, includeProducts = true, includeGames = true, includeVocabulary = true } = options;

  const db = await getDb();
  if (!db) throw new Error("Database not available");

  // Verify ownership
  const [project] = await db
    .select()
    .from(projects)
    .where(and(eq(projects.id, projectId), eq(projects.userId, userId)));

  if (!project) throw new Error("Project not found or access denied");

  const archive = archiver("zip", { zlib: { level: 9 } });
  const passThrough = new PassThrough();
  archive.pipe(passThrough);

  const safeTitle = (project.title || "EduFactory_Project")
    .replace(/[^a-zA-Z0-9_\-\s]/g, "")
    .replace(/\s+/g, "_")
    .substring(0, 50);

  const filename = `${safeTitle}_EduBundle.zip`;

  // ─── README ───────────────────────────────────────────────────────────────
  const readmeContent = generateReadme(project);
  archive.append(readmeContent, { name: "README.md" });

  // ─── PRODUCTS ─────────────────────────────────────────────────────────────
  if (includeProducts) {
    const productList = await db
      .select()
      .from(products)
      .where(eq(products.projectId, projectId));

    for (const product of productList) {
      const content = generateProductMarkdown(product);
      const productFolder = `products/${product.type}/`;
      const productFilename = `${productFolder}${product.type}_${product.id}.md`;
      archive.append(content, { name: productFilename });

      // Also generate a plain text version
      const plainText = generateProductPlainText(product);
      archive.append(plainText, { name: `${productFolder}${product.type}_${product.id}.txt` });
    }

    // Products index
    const indexContent = generateProductsIndex(productList);
    archive.append(indexContent, { name: "products/INDEX.md" });
  }

  // ─── VOCABULARY ───────────────────────────────────────────────────────────
  if (includeVocabulary) {
    const vocab = await db
      .select()
      .from(vocabularyItems)
      .where(eq(vocabularyItems.projectId, projectId));

    if (vocab.length > 0) {
      const vocabCsv = generateVocabCSV(vocab);
      archive.append(vocabCsv, { name: "vocabulary/vocabulary_list.csv" });

      const vocabAnki = generateAnkiDeck(vocab);
      archive.append(vocabAnki, { name: "vocabulary/anki_import.txt" });

      const vocabMarkdown = generateVocabMarkdown(vocab);
      archive.append(vocabMarkdown, { name: "vocabulary/vocabulary_reference.md" });
    }
  }

  // ─── GAMES ────────────────────────────────────────────────────────────────
  if (includeGames) {
    const gameList = await db
      .select()
      .from(games)
      .where(eq(games.projectId, projectId));

    if (gameList.length > 0) {
      const gamesIndex = generateGamesIndex(gameList);
      archive.append(gamesIndex, { name: "games/GAMES_LINKS.md" });
    }
  }

  // ─── MARKETPLACE CARD ─────────────────────────────────────────────────────
  const marketplaceCard = generateMarketplaceCard(project);
  archive.append(marketplaceCard, { name: "marketplace/product_listing.md" });

  const etsy = generateEtsyListing(project);
  archive.append(etsy, { name: "marketplace/etsy_listing.txt" });

  const tpt = generateTPTDescription(project);
  archive.append(tpt, { name: "marketplace/tpt_description.txt" });

  archive.finalize();

  return { stream: passThrough, filename };
}

